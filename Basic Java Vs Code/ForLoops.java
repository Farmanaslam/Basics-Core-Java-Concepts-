import java.util.*;
class ForLoops{
public static void main(String[] args) {


    //For Loop Syntax...........
    int n=4;
    for(int i=n;i>0;i--){
        for(int j=0;j<i;j++){
            System.out.print("*");
          
        }
        
    }
    System.out.println("/n");
}


}